    public void obtenerLicenciaturas(){      
        cmbListaLicenciaturas.removeAllItems();
        AdministrarLicenciaturas opL = new AdministrarLicenciaturas();
        LinkedList<Licenciatura> lista = opL.listaLicenciaturas();        
        for (Licenciatura licenciatura : lista) {
            cmbListaLicenciaturas.addItem(licenciatura);
        }
    }
    
